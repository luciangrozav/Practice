package org.fasttrackit;

public class Method {

    static int operation(int a, int b)
    {
        return (a*b)/2;
    }

    static double operation(double a, double b)
    {
        return (a*b)/2;
    }

    static double operation (double a, double b, double c)
    {
        return (a*b*c)/3;
    }

    int methoddynamic(int a)
    {
        return a*a;
    }

}
